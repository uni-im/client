class Agent {
  String name;
  String status;

  Agent(this.name);

  factory Agent.random() {
    return new Agent(_randomName());
  }

  static String _randomName() {
    Random r = new Random();

    var capitalize = (String s) => "${s[0].toUpperCase()}${s.substring(1)}";
    var firstName = capitalize(left[r.nextInt(left.length - 1)]);
    var lastName = capitalize(right[r.nextInt(right.length - 1)]);

    return "${firstName} ${lastName}";
  }
}
